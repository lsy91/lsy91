package forEx;

public class ForEx1 {

	public static void main(String[] args) {
		
		for(int i=2; i<11; i+=2) {
			System.out.println("i :" + i);
		}
		for(int j=0; j<11; j++) {
			System.out.print(j);
		}
		
		System.out.println();
		
		for(int k=0; k<11; k++) {
			System.out.println(k+" ");
		}

	}

}
