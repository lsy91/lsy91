package commit;

public class NumQuiz2 {

	public static void main(String[] args) {
		
		byte a = 100;
		short b = 10000;
		int c = 1000000000;
		long d = 10000000000l;
		char e = 'C';
		
		System.out.println(a); // 100
		System.out.println(b); // 10000
		System.out.println(c); // 1000000000
		System.out.println(d); // 10000000000
		System.out.println(e); // C

	}

}
